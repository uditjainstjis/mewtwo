def build_adapters_index():
    out = ["# Adapters Index — every PEFT adapter, with training methodology\n\n"]
    out.append("Generated by `_restructure/build_indexes.py`.\n\n")

    with open('/home/learner/Desktop/mewtwo/_restructure/all_adapters.txt') as f:
        configs = [l.strip() for l in f if l.strip()]

    # Group by base model
    by_base = defaultdict(list)
    for cfg_path in configs:
        full = ROOT / cfg_path.lstrip('./')
        cfg = safe_read_json(full)
        if not cfg:
            continue
        base = cfg.get('base_model_name_or_path', 'unknown')
        rank = cfg.get('r', '?')
        target = ','.join(cfg.get('target_modules', []))
        adapter_dir = full.parent
        rel = str(adapter_dir).replace(str(ROOT) + '/', '')
        # Adapter weight file
        wf = adapter_dir / 'adapter_model.safetensors'
        wf_size = file_size_human(wf) if wf.exists() else "—"
        by_base[base].append({
            'path': rel,
            'rank': rank,
            'target': target,
            'weight_size': wf_size,
        })

    out.append(f"**Total adapters:** {len(configs)} across {len(by_base)} base models.\n\n")

    out.append("---\n\n## By base model\n\n")
    for base in sorted(by_base):
        out.append(f"### `{base}`\n\n")
        adapters = by_base[base]
        out.append(f"({len(adapters)} adapters)\n\n")
        out.append("| Path | Rank | Target | Weight size |\n|---|---|---|---|\n")
        for a in sorted(adapters, key=lambda x: x['path']):
            out.append(f"| `{a['path']}` | {a['rank']} | {a['target']} | {a['weight_size']} |\n")
        out.append("\n")

    out.append("---\n\n## Canonical adapters used by inference / benchmarks\n\n")
    out.append("These are the adapters loaded by `src/demo/server.py`, `scripts/token_router_eval.py`, etc.\n\n")
    out.append("| Domain | Path | Notes |\n|---|---|---|\n")
    out.append("| Math | `checkpoints/nemotron_lori/adapters/math/best/` | Bit-identical to `hf_publish/math/` (hardlinked) |\n")
    out.append("| Code | `checkpoints/nemotron_lori/adapters/code/best/` | Bit-identical to `hf_publish/code/` (hardlinked) |\n")
    out.append("| Science | `checkpoints/nemotron_lori/adapters/science/best/` | Bit-identical to `hf_publish/science/` (hardlinked) |\n\n")

    out.append("---\n\n## Training intermediate checkpoints (preservation)\n\n")
    out.append("`checkpoints/nemotron_lori/adapters/{math,code,science}/checkpoint-N/` directories preserve\n")
    out.append("training history at every save step. Total ~3 GB across 3 domains. Not used by inference,\n")
    out.append("but kept for reproducibility — if a paper claim ever needs to be re-derived, the exact\n")
    out.append("checkpoint at every step is here.\n\n")
    out.append("If disk pressure ever forces cleanup, these are the safest deletion targets, in this order:\n")
    out.append("1. `checkpoint-N/` for N < final_step / 2 (early training, lowest reproducibility value)\n")
    out.append("2. `hidden_*/` checkpoints (emergency saves, mostly redundant)\n")
    out.append("3. `dare_sparsified/` (regeneratable from `final/` via `apply_dare_to_peft_model`)\n\n")

    (DOCS / "ADAPTERS_INDEX.md").write_text("".join(out))
    print(f"Wrote ADAPTERS_INDEX.md ({sum(len(s) for s in out)} chars)")


