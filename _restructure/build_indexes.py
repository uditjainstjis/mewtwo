def file_size_human(p):
    try:
        sz = os.path.getsize(p)
        for u in ['B', 'K', 'M', 'G']:
            if sz < 1024:
                return f"{sz:.0f}{u}"
            sz /= 1024
        return f"{sz:.1f}T"
    except OSError:
        return "?"


def safe_read_json(p, max_size=200_000):
    try:
        if os.path.getsize(p) > max_size:
            return None
        with open(p) as f:
            return json.load(f)
    except (OSError, json.JSONDecodeError):
        return None


