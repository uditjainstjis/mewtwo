def build_docs_index():
    out = ["# Docs Index — every Markdown file in the project\n\n"]
    out.append("Generated by `_restructure/build_indexes.py`.\n\n")

    with open('/home/learner/Desktop/mewtwo/_restructure/all_markdown.txt') as f:
        files = [l.strip() for l in f if l.strip()]
    files = [f for f in files if not f.startswith('./_restructure/_old_tmp_snapshot/')]

    out.append(f"**Total markdown files:** {len(files)}\n\n")

    # Group by directory
    by_dir = defaultdict(list)
    for fp in files:
        parts = fp.lstrip('./').split('/')
        d = '/'.join(parts[:-1]) if len(parts) > 1 else '(root)'
        by_dir[d].append(fp)

    # Section 1: Top-level + master docs (most important)
    important = ['(root)', 'docs', 'manuscripts', 'overnight_run', 'overnight_run/findings']
    out.append("## Master / canonical docs (read first)\n\n")
    for d in important:
        if d in by_dir:
            out.append(f"### `{d}/`\n\n")
            for fp in sorted(by_dir[d]):
                full = ROOT / fp.lstrip('./')
                sz = file_size_human(full)
                # Try to read first heading
                first_line = ''
                try:
                    with open(full) as f:
                        for line in f:
                            line = line.strip()
                            if line.startswith('#'):
                                first_line = line.lstrip('#').strip()
                                break
                            if line and not line.startswith('---'):
                                first_line = line[:80]
                                break
                except OSError:
                    pass
                name = fp.split('/')[-1]
                out.append(f"- **`{name}`** ({sz}) — {first_line}\n")
            out.append("\n")

    out.append("---\n\n## All other markdown files (organized by directory)\n\n")
    for d in sorted(by_dir):
        if d in important:
            continue
        out.append(f"### `{d}/`\n\n")
        out.append(f"({len(by_dir[d])} files)\n\n")
        for fp in sorted(by_dir[d]):
            name = fp.split('/')[-1]
            full = ROOT / fp.lstrip('./')
            sz = file_size_human(full)
            out.append(f"- `{name}` ({sz})\n")
        out.append("\n")

    (DOCS / "DOCS_INDEX.md").write_text("".join(out))
    print(f"Wrote DOCS_INDEX.md ({sum(len(s) for s in out)} chars)")


